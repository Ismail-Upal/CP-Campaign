void solve(){
    int n,ans=0, k; cin>>n>>k;
    vector<int>v(n);
    for(int i=0; i<n ;i++){
        cin>>v[i];
    }
    sort(v.begin(), v.end());
    map<int,int>mp;
    for(int i=0; i<n; i++)mp[v[i]]++;
    v.erase(unique(v.begin(), v.end()), v.end());
    int i=0, j=0;
    while(j<sz(v)){
        // if(v[j]!=v[j+1] && v[j]+1!=v[j+1])i++;
        if(j-i+1<=k){
            int cnt=0;
            for(int a=i; a<=j; a++){
                cnt+=mp[v[a]];
            }
            ans=max(ans,cnt);
            j++;
        }
        else{
            i++;
        }
    }
    // dbg(v);
    cout<<ans<<endl;
}
